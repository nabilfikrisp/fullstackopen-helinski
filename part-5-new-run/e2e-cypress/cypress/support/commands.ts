Cypress.Commands.add("dataCy", (id: string) => {
  return cy.get(`[data-cy=${id}]`);
});

Cypress.on("uncaught:exception", (err, runnable) => {
  // ignore 404 errors
  if (err.message.includes("404")) {
    return false; // prevent Cypress from failing the test
  }
});
